package kr.airport.parking.reduction.vo.table;


public class SoapXml {
	
	//요청 XML 메세지
	private String reqSoapXml;
	//응답 XML 메세지
	private String resSoapXml;
	
	
	public String getReqSoapXml() {
		return reqSoapXml;
	}
	public void setReqSoapXml(String reqSoapXml) {
		this.reqSoapXml = reqSoapXml;
	}
	public String getResSoapXml() {
		return resSoapXml;
	}
	public void setResSoapXml(String resSoapXml) {
		this.resSoapXml = resSoapXml;
	}
	
}
